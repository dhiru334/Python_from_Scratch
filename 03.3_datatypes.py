#python supports several built-in data types:
# Integers (int): Whole number (e.g., 10,-5).
# Floats (floats): Decimal numbers (e.g., 7.8,-8.3)
# Strings (str): Text data enclosed in quotes (e.g., "Helllo",'Python').
# Booleans (bool): Represents true or false.
# Lists: Ordered, mutable collections (e.g., [1,2,3]).
# Tuples: Ordered, immutable collections (e.g.,(1,2,3)).
# Sets: Unoredered collections of unique elements (e.g.,{1,2 ,4}).
# Dictionaries: Key-value pairs (e.g., {"name":"Alice","age":35}).

age = 21;
print(age)
print(type(age))

sgpa = 7.8
print(sgpa)
print(type(sgpa))

name = "Sanjee"
print(name)
print(type(name))

is_completed = True #can also be false
print(is_completed)
